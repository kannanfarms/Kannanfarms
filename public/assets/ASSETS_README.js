// This file documents how Vite serves static assets.
// 
// In development and production, files placed in /public/assets/
// are served at the URL /assets/<filename>.
//
// Your image references in code use paths like:
//   /assets/Banana 1.png      → public/assets/Banana 1.png
//   /assets/Logowoback.png    → public/assets/Logowoback.png
//
// To populate assets, copy from your KannanFarms-main directory:
